package com.example.ef_g1.Dto;

public class ReportesDto {
    private int numEmpleados;
    private int num3D;
    private int numCines;
    private int numCartelera;

    public int getNumEmpleados() {
        return numEmpleados;
    }

    public void setNumEmpleados(int numEmpleados) {
        this.numEmpleados = numEmpleados;
    }

    public int getNum3D() {
        return num3D;
    }

    public void setNum3D(int num3D) {
        this.num3D = num3D;
    }

    public int getNumCines() {
        return numCines;
    }

    public void setNumCines(int numCines) {
        this.numCines = numCines;
    }

    public int getNumCartelera() {
        return numCartelera;
    }

    public void setNumCartelera(int numCartelera) {
        this.numCartelera = numCartelera;
    }
}
